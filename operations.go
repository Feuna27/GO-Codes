package main

import "fmt"

func main() {
	// Addition
	sum := 5 + 3
	fmt.Println("Sum:", sum)

	// Subtraction
	difference := 7 - 2
	fmt.Println("Difference:", difference)

	// Multiplication
	product := 4 * 6
	fmt.Println("Product:", product)

	// Division
	quotient := 10 / 2
	fmt.Println("Quotient:", quotient)

	// Modulus (remainder)
	remainder := 15 % 4
	fmt.Println("Remainder:", remainder)
}
